	  IF(IPAR.NE.1) TS = TS + TSUM(IEKIN,JT1,JT2,IZF)
C And odd parity
	  IF(IPAR.NE.0) TS = TS + TSUM(IEKIN,JT2+1,JT1,IZF)
C For unknown parities take average of both
	  IF(IPAR.LT.0) TS = TS * 0.5
	  IF((RHOZ-RHOX).GT.-REAL_MAX) THEN
	    WX = EXP(RHOZ-RHOX) * TS
	    SUMWX = SUMWX + WX
	  ELSE
	    WX=0.
	  ENDIF
C MNH	ENDIF
	W(IEXXZF,JJZF,IZF) = WX
112	CONTINUE
	ISUMW(IZF) = 1
	SUMW(IZF) = SUMWX
114	CONTINUE

C Gamma decay
	IZF = IZFMAX + 1
	IF (IEXX.LE.1) GO TO 117
	IH5 = IEXX - 1
	IH4 = IH5 - (KIN_GAM-1)
	IF (IH4.LE.0) IH4 = 1
	JJZFU = JJ - 2
	IF (JJZFU.LE.0) JJZFU = 1
	JJZFO = JJ + 2
	IF (JJZFO.GT.JJX) JJZFO = JJX
	DO 115 JJZF=JJZFU,JJZFO
	IF(JJ.EQ.1.AND.JJZF.EQ.1.AND.JD1.EQ.0) GO TO 115
	JD = JJ - JJZF + 3
	MP = MPOL(JD)
	DO 116 IEXXZF=IH4,IH5
	WX = 0
	RHOZ = RHO (IEXXZF,JJZF,IZF)
C MNH	IF (RHOZ.GT.RHOGR) THEN
	  IEKIN = IEXX - IEXXZF
C Get parity of daughter nucleus
	  IP2 = IPARI(IEXXZF,JJZF,IZF)
C Get parity of transition
	  IF ( IP1.LE.-1 .OR. IP2.LE.-1 ) THEN
	    IPAR = -1
	  ELSE
	    IPAR = MOD( IP1+IP2, 2 )
	  ENDIF
	  TS = 0
C Add M1 and E2 transitions
	  IF(IPAR.NE.1) TS = TS + TLG (IEKIN,MP,1)
C And E1 transitions
	  IF(IPAR.NE.0) TS = TS + TLG (IEKIN,MP,2)
C For unknown parities take average of both
	  IF(IPAR.LT.0) TS = TS * 0.5
	  IF((RHOZ-RHOX).GT.-REAL_MAX) THEN
	    WX = EXP(RHOZ-RHOX) * TS * ENHGAM
	    WGAMMA = WGAMMA + WX
	  ELSE
	    WX=0.
	  ENDIF
C MNH	ENDIF
	WG (IEXXZF,JD) = WX
116	CONTINUE
115	CONTINUE

C Normalization constant and relative decay widths
117	WX = GXMIN * ENHGAM
	IF((WFISS-RHOX).LE.REAL_MAX) THEN
	  INDFISS=0
	  IF((WFISS-RHOX).GT.-REAL_MAX) THEN
	    WFISS=EXP(WFISS-RHOX)
	  ELSE
	    WFISS=0
	  ENDIF
	  SUMWX = SUMW(1)+SUMW(2)+SUMW(3)+SUMW(4)+WGAMMA+WFISS
	  IF ( SUMWX+WX.LE.ABSMIN) THEN
	    CNORM = 1
	  ELSE
	    CNORM = 1/(SUMWX+WX)
	  ENDIF
	  ZW(1) = SUMW(1) * CNORM
	  ZW(2) = SUMW(2) * CNORM
	  ZW(3) = SUMW(3) * CNORM
	  ZW(4) = SUMW(4) * CNORM
	  ZW5   = WGAMMA  * CNORM
	  ZW6   = WX      * CNORM
	  ZW7   = WFISS   * CNORM
	ELSE
	  INDFISS=1
	  ZW(1) = 0
	  ZW(2) = 0
	  ZW(3) = 0
	  ZW(4) = 0
	  ZW5   = 0
	  ZW6   = 0
	  ZW7   = 1.
	  CNORM=0.0
	ENDIF
	IZW1 = ZW(1) * 99.4 + 0.5
	IZW2 = ZW(2) * 99.4 + 0.5
	IZW3 = ZW(3) * 99.4 + 0.5
	IZW4 = ZW(4) * 99.4 + 0.5
	IZW5 = ZW5   * 99.4 + 0.5
	IZW7 = ZW7   * 99.4 + 0.5
	IZW8 = 0
	IZW9 = 0
	IF (KOUTL.GE.0) GO TO 1205
C Lifetimes
	TAU = ABSMIN
	IF (SUMWX.LT.ABSMIN) GO TO 1204
	IF (INDFISS.EQ.0) TAU = 4.12E-21 / SUMWX
