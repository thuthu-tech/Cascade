	E2 = EXMIN(IPS) + IEXXL(IPS)*ESTEP
	E3 = EXMIN(IPS) + IEXXS(IPS)*ESTEP
	E4 = EXMAX(IPS)
	CJ1 = JMIN(IPS) + 1
	CJ2 = JMAX(IPS)
	IF (MOD(IAA,2).EQ.0) GO TO 155
	CJ1 = CJ1 + .5
	CJ2 = CJ2 + .5
