	  J_TMP = 0
	  WT_TMP = 0.
20	  READ(*,21) IP,IL(1),J_TEMP(II),WT_TEMP(II)
21	  FORMAT(2A,I3,F8.0)
	  IF(IP.EQ.'E') THEN
C Test if out-of-range has occurred
	    IF ((II.EQ.L_DIM).AND.(J_TMP.NE.0.)) THEN
	      WRITE (1, 215) JCN, J_TEMP(L_DIM)
215           FORMAT('WARNING: arrays J_TEMP and WT_TEMP too small!'
     1         ,' JCN used in calculation = ', I3
     2         ,' J_MAX read in = ',I3)
C Copy values from temporary variable back into arrays
	      IP = IP_TMP
	      IL(1) = IL_TMP
	      J_TEMP(L_DIM) = J_TMP
	      WT_TEMP(L_DIM) = WT_TMP
	    ENDIF
	    GO TO 22
	  ENDIF
	  IF (II.LT.L_DIM) THEN
	    JCN=MAX(J_TEMP(II),JCN)
	    II=II+1
	  ELSEIF ((II.EQ.L_DIM).AND.(J_TMP.EQ.0.)) THEN
C Copy values for index L_DIM in temporary variables
	    IP_TMP = IP
	    IL_TMP = IL(1)
	    J_TMP = J_TEMP(II)
	    JCN=MAX(J_TEMP(II),JCN)
