	READ *, IZE(4),IAE(4),JE(4),IPE(4),EXC4,IZFF,DAF,FFB
	IF(JCN.NE.999) THEN
	  READ *, CL0,DIFF,SIGMCN
	ELSE
	  INDENT=1
	  JCN=1
