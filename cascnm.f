19	INRHOP = MINUS
	INLEVL = MINUS
	INDENT=0
	IND1 = 0
	IND2 = 0
C Input
	READ (*,'(A)',END=1000) TEXT
	READ *, IZP,IAP,IZT,IAT,ELAB
