	WX = CNORM * W (IEXXZF,JJZF,IZF)
	SE(IEKIN,IZF) = SE(IEKIN,IZF) + WX
1215	WT (IEXXZF,JJZF,IZF) = WT (IEXXZF,JJZF,IZF) + WX
