	  XIE1=2.09E-5*IZCN*(IACN-IZCN)/ACN
	ENDIF
	WRITE(1, 5558)XIE1
 5558	FORMAT('/ XIE1=',E10.4)
	XIM1 = XYM1 * 1.32E-7
	XIE2 = XYE2 * 0.308E-12 * ACN23*ACN23
	IF (IND2.EQ.1) THEN
