C MNH	multiplication.
	CNORM = W0 * CNORM

C Fission cross section
	WQF(IPS) = WQF(IPS) + ZW7 * W0

C Normalization of W, summation onto WT
C Particle decay
	DO 123 IZF=1,IZFMAX
