	OPEN(UNIT=12,STATUS='UNKNOWN',FILE='LEVDENS.TMP'
     1	,FORM='UNFORMATTED',ACCESS='DIRECT'
     1  ,RECL=L_EX_DIM*L_DIM/2*4)
