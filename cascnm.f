	ECM = ELAB*IAT/ACN
	IF (IAP.NE.0) CSL = 655.17/(ARED*ECM)
	IF (IAP.NE.0 .AND. JCN.LT.0 .AND. CL0.LE.0.01 .AND.
     1   SIGMCN.GT.0.01) CL0 = SQRT(SIGMCN/CSL) - 0.5
	IF (IAP.NE.0 .AND. JCN.LT.0 .AND. ABS(CL0).LT.0.01)
     1   CL0 = CLFUS(IZP,IAP,IZT,IAT,ELAB)
