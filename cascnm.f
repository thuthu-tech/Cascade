c	OPEN(UNIT=13,STATUS='unknown',FILE='SYS$SCRATCH:POPULMAT.TMP'
	OPEN(UNIT=13,STATUS='UNKNOWN',FILE='POPULMAT.TMP'
     1	,FORM='UNFORMATTED',ACCESS='DIRECT'
     1  ,RECL=L_EX_DIM*L_DIM/2*4)
