	  DO 136 IEKIN=1,KIN_PAR
	  SE(IEKIN,5)=0.
	  DO 136 IZF=1,3
136	  SE(IEKIN,IZF)=0.
	ELSE
	  DO 139 IZF=1,IZFMAX
	  IF (ICZF(IZF).EQ.0) GO TO 139
	  IPSZF = IPSZO(IZF,IPS)
	  CALL GETW(IPSZF,IT)
	  IF (IT.EQ.0) GO TO 138
	  JJXZF = JJMAX(IPSZF)
	  IH2 = IEXXMX(IPSZF)
	  DO 137 JJZF=1,JJXZF
	  DO 137 IEXXZF=1,IH2
137	  WT(IEXXZF,JJZF,IZF) = WT(IEXXZF,JJZF,IZF) + WT0(IEXXZF,JJZF)
138	  CALL STOREW(IPSZF,IZF)
139	  CONTINUE
	ENDIF

C Compress W-table
	IF (IWL.GT.20) CALL CLEAN

140	CONTINUE
C ****************************************************************
C ****************************************************************

C General output
	CALL OUTS (0,KEVAP,KGAMMA,EMINT,TEXT,0.,0.,SCN(1))
