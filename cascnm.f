        EQUIVALENCE (WQR(1,1),WGZ(1,1))
	DIMENSION WT_TEMP(L_DIM),J_TEMP(L_DIM)
	DIMENSION WQA(30),WQ(NUCL),WQT(NUCL),WQF(NUCL),GMULT(NUCL)
