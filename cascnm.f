25	WT(1,L_DIM,IZF) = IPSZF
	IF (IZF.EQ.1) THEN
	  WRITE (13,REC=2*IT-1) WT1A
	  WRITE (13,REC=2*IT  ) WT1B
	ELSEIF (IZF.EQ.2) THEN
	  WRITE (13,REC=2*IT-1) WT2A
	  WRITE (13,REC=2*IT  ) WT2B
	ELSEIF (IZF.EQ.3) THEN
	  WRITE (13,REC=2*IT-1) WT3A
