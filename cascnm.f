	EQUIVALENCE (WT0A(1),WT0(1,        1))
	EQUIVALENCE (WT0B(1),WT0(1,L_DIM/2+1))

	DO 20 IT=1,ITMAX
	IF (INHWT(IT).EQ.IPS) GO TO 25
20	CONTINUE
	IT0 = 0
	RETURN

25	IT0 = IT
	READ(13,REC=IT0*2-1) WT0A
	READ(13,REC=IT0*2  ) WT0B
