	COMMON/C7/ WG(L_EX_DIM,5),WGZ(L_EX_DIM,L_DIM),
     1     LZ(L_EX_DIM,L_DIM),LZF(L_EX_DIM,L_DIM)
	COMMON/C7/ SE(KIN_PAR,5),SESUM(KIN_MAX,5)

